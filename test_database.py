import pytest
import importlib

database = importlib.import_module('database')
engine = importlib.import_module('reminder-engine')     

store = database.Store()

#test importFromPickle handles error for non-existant files
def test_importFromPickle_failBadName():
    with pytest.raises(Exception) as e_info:
        store.importFromPickle("non-existant file")

#test importFromPickle, depends on import-test.pickle
def test_importFromPickle_passGoodName(capsys):
    store.importFromPickle("import-test")
    store_list = (store._Store__reminders)
    for reminder in store_list:
        print(reminder.__dict__)
    captured = capsys.readouterr()
    assert captured.out == "{'_id': 0, '_Reminder__text': 'a', '_Reminder__tags': ['a']}\n"

#test store list is returned
def test_reminders():
    store.importFromPickle("import-test")
    store_list = (store._Store__reminders)
    assert type(store.reminders) is list
    #returns store.reminders == ["Reminder ID: 0 | Tags: ['a']\nDescription: a\n"]

#test that searchByID throws non-integer values
def test_searchByID_withString():
    with pytest.raises(Exception) as e_info:
        store.searchByID("clearly a string")

#test that searchByID passes integer values, returns object
def test_searchByID_withInt():
    with pytest.raises(Exception) as e_info:
        store.importFromPickle("import-test")
        store_list = (store._Store__reminders)
        assert store.searchByID("0") is object

#test that search handles empty strings, throws value error
def test_search_throwsEmptyString():
    with pytest.raises(Exception) as e_info:
        store.search("", None, None)